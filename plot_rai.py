def parse_json2(data: List[Dict[str, Any]]) -> Dict[str, List[float]]:
    """
    Parses data from the second JSON format (list of dicts) into a unified structure.

    Args:
        data: The list of dictionaries, where each dict is an epoch.

    Returns:
        A dictionary with standardized keys for plotting.
    """
    epochs = [d["epoch"] for d in data]
    train_loss = [d["train_loss"] for d in data]
    val_loss = [d["val_loss"] for d in data]
    test_loss = [d["test_loss"] for d in data]
    train_acc = [d["train_acc1"] for d in data]
    val_acc = [d["val_acc1"] for d in data]
    test_acc = [d["test_acc1"] for d in data]

    return {
        "epochs": epochs,
        "train_loss": train_loss,
        "val_loss": val_loss,
        "test_loss": test_loss,
        "train_acc": train_acc,
        "val_acc": val_acc,
        "test_acc": test_acc,
    }


# --- Plotting Function ---


def plot_metrics(data: Dict[str, List[float]], title_prefix: str):
    """
    Creates and saves two plots: Loss vs. Epoch and Accuracy vs. Epoch.

    Args:
        data: The parsed dictionary containing all metric data.
        title_prefix: A string to prepend to the plot titles (e.g., "JSON 1").
    """
    epochs = data["epochs"]

    # Create the Loss vs. Epoch plot
    plt.figure(figsize=(10, 6))
    plt.plot(
        epochs,
        data["train_loss"],
        label="Train Loss",
    )
    plt.plot(
        epochs,
        data["val_loss"],
        label="Validation Loss",
    )
    plt.plot(
        epochs,
        data["test_loss"],
        label="Test Loss",
    )
    plt.title(f"{title_prefix} - Epoch vs. Loss", fontsize=16, fontweight="bold")
    plt.xlabel("Epoch", fontsize=12)
    plt.ylabel("Loss", fontsize=12)
    plt.legend()
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(f"{title_prefix.replace(' ', '_').lower()}_loss_plot.png")
    plt.show()  # Display the plot

    # Create the Accuracy vs. Epoch plot
    plt.figure(figsize=(10, 6))
    plt.plot(
        epochs,
        data["train_acc"],
        label="Train Accuracy",
    )
    plt.plot(
        epochs,
        data["val_acc"],
        label="Validation Accuracy",
    )
    plt.plot(
        epochs,
        data["test_acc"],
        label="Test Accuracy",
    )
    plt.title(f"{title_prefix} - Epoch vs. Accuracy", fontsize=16, fontweight="bold")
    plt.xlabel("Epoch", fontsize=12)
    plt.ylabel("Accuracy (%)", fontsize=12)
    plt.legend()
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(f"{title_prefix.replace(' ', '_').lower()}_acc_plot.png")
    plt.show()  # Display the plot


